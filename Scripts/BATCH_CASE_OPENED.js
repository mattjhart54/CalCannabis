function mainProcess() {
try{
	var recCount = 0;
	var getCapResult = aa.cap.getCapIDsByAppSpecificInfoField("Case Opened By","Science Provisional");
	if (getCapResult.getSuccess())
		var apsArray = getCapResult.getOutput();
	else
		{ logDebug( "**ERROR: getting caps by app type: " + getCapResult.getErrorMessage()) ; return null }
		

	for (aps in apsArray)
		{
		capId = aa.cap.getCapID(apsArray[aps].getCapID().getID1(),apsArray[aps].getCapID().getID2(),apsArray[aps].getCapID().getID3()).getOutput();
		myAppTypeString = aa.cap.getCap(capId).getOutput().getCapType().toString();

		isMatch = true;
		logDebug("myAppTypeArray: " + myAppTypeString);
		if (!myAppTypeString.equals("Licenses/Cultivator/License Case/NA")){
			isMatch = false;
		}
		
		if (isMatch)
			{
			logDebug("Returned " + capId.getCustomID());
			editAppSpecific("Case Opened By","Science - Provisional",capId);
			recCount++;
			}
		}
		
		logDebug("Processed: " + recCount + " Records");

}catch (err){
	logDebug("ERROR: " + err.message + " In " + batchJobName);
	logDebug("Stack: " + err.stack);
}}
	
